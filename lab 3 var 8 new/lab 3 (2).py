# Зчитування кількості кутів
n = int(input("Введіть кількість кутів правильного багатокутника: "))

# Перевірка на валідність значення n (має бути більше 2)
if n <= 2:
    print("Кількість кутів має бути більше двох!")
else:
    # Обчислення кута
    angle = (n - 2) * 180 / n
    print(f"Величина одного внутрішнього кута: {angle:.2f} градусів")
